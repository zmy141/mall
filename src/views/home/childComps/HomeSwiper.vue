<template>
    <swiper>
      <swiper-item  v-for = '(item,index) in banners' :key = 'index'>
        <a :href= "item.link">
          <img :src= "item.image" alt="" @load= 'imageLoad'>
        </a>
      </swiper-item>
    </swiper>
</template>

<script>
import {Swiper,SwiperItem} from 'components/common/swiper'

export default {
    name:'HomeSwiper',
    props:{
        //类型是对象或者数组时，默认值必须是一个函数
        banners:{
            type:Array,
            default(){
                return []
            }
        }
    },
    data(){
      return{
        isLoad:false
      }
    },
    components:{
        Swiper,
        SwiperItem
    },
    methods:{
      imageLoad(){
        if(!this.isLoad){
          this.$emit('swiperImageLoad')
          this.isLoad=true
        }       
      }
    }
}
</script>

